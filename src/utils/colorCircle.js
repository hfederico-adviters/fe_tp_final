export const colorCircle = (color) => {
  if (color === 1) {
    const vacaciones = "#D45BFF";
    return vacaciones;
  }
  if (color === 4) {
    const tramite = "#F7FC00;";
    return tramite;
  }
  if (color === 2) {
    const licencia = "#5BFF62;";
    return licencia;
  }
  if (color === 3) {
    const diaDeEstudio = "#5BCEFF;";
    return diaDeEstudio;
  }
};
